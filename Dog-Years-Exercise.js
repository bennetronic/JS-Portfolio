// My Age
const myAge = 32;

// First two years
let earlyYears = 2;
earlyYears *= 10.5;

// My age minus earlyYears
let laterYears = myAge - 2;

// Dog years for laterYears
laterYears *= 4;

console.log(earlyYears);
console.log(laterYears);

// Add early years and Later years
let myAgeInDogYears = earlyYears + laterYears;

// My name in lowercase
let myName = 'JAMES BENNETTS'.toLowerCase();

// Log my name and age in dog years
console.log(`My name is ${myName}. I am ${myAgeInDogYears} years old in dog years.`)